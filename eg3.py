num1 = 7.5
num2 = 6.3


sum = num1 + num2


print('The sum of {0} and {1} is {2}'.format(num1, num2, sum))

sub = num1 - num2

print('The subtraction {0} and {1} is {2}'.format(num1, num2, sub))

mul = num1*num2

print('Multiplication value {0} and {1} is {2}'.format(num1, num2, mul))
